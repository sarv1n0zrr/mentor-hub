class FavoriteItem {
  final String id;
  final String name;
  final String description;
  final String price;
  final double rating;
  final String photo;
  final String type;

  FavoriteItem({
    required this.id,
    required this.name,
    required this.description,
    required this.price,
    required this.rating,
    required this.photo,
    required this.type,
  });
}
